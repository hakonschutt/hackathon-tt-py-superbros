def generate_interface_adapter(base_tree: Tree, roai_path: Path) -> str:
    """Generate interface adapter methods by analyzing the TS base class.

    Examines the base class to understand what public methods exist,
    then generates corresponding Python methods that bridge between
    the wrapper's abstract interface and the translated internal methods.

    Args:
        base_tree: Parsed AST of the TS base class.
        roai_path: Path to the ROAI calculator source (for reference).

    Returns:
        Python source code for the interface adapter methods.
    """
    # Analyze base class methods
    methods = _find_class_methods(base_tree)
    public_methods = [m["name"] for m in methods if m["is_public"]]

    # Generate adapter methods based on what the base class provides
    all_lines: list[str] = []

    # Always need the snapshot orchestrator
    all_lines.extend(_build_compute_snapshot())
    all_lines.append("")

    # Generate each interface method
    method_builders = {
        "getPerformance": _build_get_performance,
        "getInvestments": _build_get_investments,
        "getSnapshot": _build_get_holdings,  # maps to get_holdings
        "getDividendInBaseCurrency": _build_get_dividends,
    }

    # Always generate all interface methods (required by abstract base)
    all_lines.extend(_build_get_performance())
    all_lines.append("")
    all_lines.extend(_build_get_investments())
    all_lines.append("")
    all_lines.extend(_build_get_holdings())
    all_lines.append("")
    all_lines.extend(_build_get_details())
    all_lines.append("")
    all_lines.extend(_build_get_dividends())
    all_lines.append("")
    all_lines.extend(_build_evaluate_report())

    return "\n".join(all_lines)
