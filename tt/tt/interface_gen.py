    for s in short:
        lines.append(f'{i3}{s} = Decimal("0")')
    lines.append(f"{i3}for sy, vl in values_by_symbol.items():")
    for s, k in zip(short, val_keys):
        lines.append(f'{i4}{s} += vl.get("{k}", {{}}).get(ds, Decimal("0"))')
    lines.append(f'{i3}npi = 0 if twv == 0 else float(npv / twv)')
    lines.append(f'{i3}npiw = 0 if twvw == 0 else float(npvw / twvw)')
